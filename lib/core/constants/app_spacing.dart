abstract final class AppSpacing {
  // Base-4 spacing scale
  static const double xs  = 4.0;
  static const double sm  = 8.0;
  static const double md  = 12.0;
  static const double lg  = 16.0;
  static const double xl  = 20.0;
  static const double xxl = 24.0;
  static const double x3l = 32.0;
  static const double x4l = 40.0;
  static const double x5l = 48.0;
  static const double x6l = 64.0;

  // Semantic aliases
  static const double pagePadding   = xl;   // 20 — padding lateral de página
  static const double cardPadding   = lg;   // 16 — padding interno de cards
  static const double sectionGap    = xxl;  // 24 — gap entre seções
  static const double itemGap       = md;   // 12 — gap entre itens de lista
  static const double inlineGap     = sm;   // 8  — gap inline (ícone + texto)
}
